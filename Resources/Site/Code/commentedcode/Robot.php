<div class="robotBlurb" id="Design">
<h3 style="text-align: center;">Design</h3>
<p>Starting with a simple frame and six wheel tank drive, we then designed a unique robot to fit our strategy requirements. We use a large belt to lift and hold balls that will be passed onto the shooter. We chose to create a shooter similar to pitching machine, as it proved the most versitile and consistent.  In order to cross the middle of the field, we designed a unique arm that allows our robot to climb onto and pass over the bridges. We also built skid plates to allow the robot to move over the middle "bump".</p>
</div>

<div class="robotBlurb" id="Strategy">
<h3 style="text-align: center;">Strategy</h3>
<p>This year's <i>FIRST</i> Robotics Competition provides unique challenges. The game is remarkably similar to basketball, and requires us to design and build a robot that can shoot a basket, move over obstacles on the court, and pick up basketballs. We chose to create an offensive robot, meaning that it focuses on scoring, although it is versatile enough that it can maintain other play styles if they prove advantageous. </p>
</div>

